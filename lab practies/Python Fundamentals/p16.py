# List of fruits
list1 = ['apple', 'banana', 'mango']

# Loop with continue
for fruit in list1:
    if fruit == 'banana':
        continue  # skip banana
    print(fruit)

# List of fruits
list2 = ['apple', 'banana', 'mango']

# Loop with break
for fruit in list2:
    if fruit == 'banana':
        break  # exit loop when banana is found
    print(fruit)
